num = int(input('Digite um número: '))
dobro = num * 2
triplo = num * 3
raiz_quadrada = num ** (1/2)
print('O dobro de {} é {}, seu triplo é {} e sua raiz quadrada é {:.2f}'.format(num, dobro, triplo, raiz_quadrada))

