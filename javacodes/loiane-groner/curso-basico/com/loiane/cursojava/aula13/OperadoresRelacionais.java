package com.loiane.cursojava.aula13;

public class OperadoresRelacionais {
    public static void main(String[] args) {

        int valor1 = 1;
        int valor2 = 2;
        System.out.println("valor1 é igual a valor 2? " + (valor1 == valor2));
        System.out.println("valor1 é diferente do valor 2? " + (valor1 != valor2));
        System.out.println("valor1 é maior que o valor 2? " + (valor1 > valor2));
        System.out.println("valor1 é menor que o valor 2? " + (valor1 < valor2));
        System.out.println("valor1 é maior ou igual ao valor 2? " + (valor1 >= valor2));
        System.out.println("valor1 é menor ou igual ao valor 2? " + (valor1 <= valor2));
    }
}
